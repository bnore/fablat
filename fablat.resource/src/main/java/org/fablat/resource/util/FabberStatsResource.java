public class FabberStatsResource {

	private Integer nWorkshopsCoordinator;
	private Integer nWorkshopsCollaborator;
	private Integer nReplicationsCoordinator;
	private Integer nReplicationsCollaborator;

	public Integer getnWorkshopsCoordinator() {
		return nWorkshopsCoordinator;
	}

	public void setnWorkshopsCoordinator(Integer nWorkshopsCoordinator) {
		this.nWorkshopsCoordinator = nWorkshopsCoordinator;
	}

	public Integer getnWorkshopsCollaborator() {
		return nWorkshopsCollaborator;
	}

	public void setnWorkshopsCollaborator(Integer nWorkshopsCollaborator) {
		this.nWorkshopsCollaborator = nWorkshopsCollaborator;
	}

	public Integer getnReplicationsCoordinator() {
		return nReplicationsCoordinator;
	}

	public void setnReplicationsCoordinator(Integer nReplicationsCoordinator) {
		this.nReplicationsCoordinator = nReplicationsCoordinator;
	}

	public Integer getnReplicationsCollaborator() {
		return nReplicationsCollaborator;
	}

	public void setnReplicationsCollaborator(Integer nReplicationsCollaborator) {
		this.nReplicationsCollaborator = nReplicationsCollaborator;
	}
}
